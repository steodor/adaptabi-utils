if (!Array.prototype.sortByField) {
    Object.defineProperty(Array.prototype, 'sortByField', {
        value: function (field, field2 = null, descending = false) {
            if (this == null) {
                throw new TypeError('this is null or not defined');
            }
            return this.sort((a, b) => {
                let a2 = field2 && a && a[field2],
                    b2 = field2 && b && b[field2];
                a = a && a[field];
                b = b && b[field];

                return (a > b ? 1 : (a < b ? -1 : (!field2 ? 0 : a2 > b2 ? 1 : (a2 < b2 ? -1 : 0)))) * (descending ? -1 : 1);
            })
        },
    });
}

// adapted from https://stackoverflow.com/a/4673436/334110
if (!String.prototype.printf) {
    String.prototype.printf = function () {
        var args = arguments;
        return this.replace(/{(\d+)}/g, function (match, number) {
            return typeof args[number] != 'undefined'
                ? args[number]
                : match
                ;
        });
    };
}

if (!String.prototype.startsWith) {
    String.prototype.startsWith = function (searchString, position = 0) {
        return this.substr(position).indexOf(searchString) === 0;
    }
}
